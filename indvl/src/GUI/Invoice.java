    private void inv_search_txtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inv_search_txtKeyTyped
        try {
            String invid2 = inv_search_txt.getText();
            ResultSet rs = database.db.search("SELECT invoice_item.selling_price,invoice_item.QTY,product.p_id, product.brand,product.model,warranty.end_date FROM invoice_item INNER JOIN product ON invoice_item.product_p_id = product.p_id JOIN warranty ON invoice_item.warranty_id = warranty.w_id WHERE invoice_item.invoice_inv_id = '" + invid2 + "'");
            String pidt = null;
            String brdnt = null;
            String mdlt = null;
            String wrrnt = null;
            int prcet = 0;
            int qtyt = 0;
            while (rs.next()) {
                pidt = rs.getString("product.p_id");
                brdnt = rs.getString("product.brand");
                mdlt = rs.getString("product.model");
                wrrnt = rs.getString("warranty.end_date");
                prcet = Integer.parseInt(rs.getString("invoice_item.selling_price"));
                qtyt = Integer.parseInt(rs.getString("invoice_item.QTY"));
                int totalt = prcet * qtyt;

                DefaultTableModel dtm = (DefaultTableModel) ino_tb1.getModel();

                Object[] row1 = {pidt, brdnt, mdlt, wrrnt, prcet, qtyt, totalt};
                dtm.setRowCount(0);
                dtm.addRow(row1);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_inv_search_txtKeyTyped

