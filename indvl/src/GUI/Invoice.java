    void idgen() {
        try {
            ResultSet rs1 = database.db.search("SELECT COUNT(inv_id) AS x FROM invoice");
            if (rs1.next()) {
                int x = Integer.parseInt(rs1.getString("x"));
                x++;
                inv_id.setText("INV00" + x);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

