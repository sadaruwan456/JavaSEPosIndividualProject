    boolean validateNic(String nic) {
        boolean nic_state = true;
        if (nic.length() == 10) {
            if (nic.contains("v") || nic.contains("x") || nic.contains("V") || nic.contains("X")) {
                try {
                    String number = nic.substring(0, 9);
                    Integer.parseInt(number);
                    int days = Integer.parseInt(nic.substring(2, 5));
                    if (366 < days) {
                        nic_state = days > 500;
                    }
                    if (days > 866) {
                        nic_state = false;
                    }
                } catch (Exception e) {
                    nic_state = false;
                }

            } else {
                nic_state = false;
            }
        } else {
            if (nic.length() == 12) {
                try {
                    Long.parseLong(nic);
                    int days = Integer.parseInt(nic.substring(4, 7));
                    if (366 < days) {
                        nic_state = days > 500;
                    }
                    if (days > 866) {
                        nic_state = false;
                    }
                } catch (Exception e) {
                    nic_state = false;
                }

            } else {
                nic_state = false;
            }
        }

        return nic_state;
    }

