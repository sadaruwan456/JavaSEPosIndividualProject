    boolean validateEmail(String email) {
        boolean status;
        Pattern ptn = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher matcher = ptn.matcher(email);

        if (matcher.matches()) {

            status = true;
        } else {
            status = false;

        }
        return status;

    }

